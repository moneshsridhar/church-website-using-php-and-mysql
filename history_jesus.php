	<h3 style="color: black; font-weight: bold;" >DEVOTION HISTORY OF JESUS</h3>
	<p style="color: black;">&nbsp &nbspThe childhood of Jesus is an active devotion in the Catholic church for nearly a thousand years. Christians always longed to show their deep reverence to the incarnate God, Jesus Christ. This veneration was stimulated aside from the Gospel also by apocryphal writings like f. i. James's and Thomas's pseudo gospels. The Child Jesus was adored by Church Fathers like St. Athanasius or St. Jerome. Other great worshipers of the Childhood Jesus's are St. Bernard of Chlairvaux, St. Francis of Assisi and St. Anthony of Padua. During the Baroque space it was first of all St. Teresa of Avila, who always traveled with her sculpture of the Infant Jesus during founding new convents. In Spain this cult had great devotion.
Individual depictions of the Child Jesus had their beginning in the 14th century. The oldest sculpture can be found in Germany where representative image had started in the visionary environment of women's convents. Usually the Child Jesus is holding in his hands various objects. With the right he mostly blesse, in the left hand he either holds a bird, an apple, a book, a cross or bunch of grapes, which is a reference to the Eucharisty. (J 15,1-11) In the middle Ages the sculptures were made of wood, where as in the baroque period they are of different materials like wax, ivory, bronze and others. In the baroque workshops, it became a habit to dress the statues.
</p>
<h3 style="color: black; font-weight: bold;">THE INFANT JESUS OF PRAGUE</h3>
<p style="color: black;">&nbsp &nbspThe cult of the Childhood Jesus's linked to the Baroque period. In it the Child Jesus plays the main part. Its history starts in Spain. It is the work of an unknown artist. It is believed that it comes from a convent between Cordoba and Seville and is a copy of a venerated wooden sculpture. There Dona Isabela Manrique de Lara a Mendoza obtained it. The sculpture then traveled as a wedding gift to her daughter Maria Manrique de Lara. She married a prominent Czech Nobleman Vojtech of Pernstejn. Again as a wedding gift to her daughter Polyxena who married Vilem of Rozumberk. She took the sculpture with her to her second mariage with Zdenek Vojtech of Lobkowicz. After his death, she donated the statue of the Child Jesus to the monastery of the Teresian Carmelites near the church of Virgin Mary Victorious in Prague Mala Strana. There it was placed first in the chapel, where it was venerated mainly by the novices.
In the year 1631 the Saxons invaded Prague and the plundering did not bypass the monastery. The sculpture of the Child Jesus was thrown out with the waste behind the High Altar. Here Father Cyril a Matre Dei found it when he returned to the abandoned church in Mala Strana from MÃ¼nchen. The sculpture was damaged. After many attempts in vain, Father Cyril was happy to get some money from a Prague citizen for repairing the broken arms. The Infant Jesus thus became again the object of worship and a number of miracles were linked to the statue and also the protection of Prague from the Swedes.
</p>

<p style="color: black;">&nbsp &nbspThe family Martinic greatly worshiped the Infant Jesus. In 1651, under their leadership the sculpture was taken in procession from the church of Our Lady Victorious to all the churches in Prague. At this festivity it received the title Gratious. In 1655 the bishop of Prague crowned the Infant Jesus with a crown, that was ordered by Bernard Ignac of Martinic. In memory of this coronation a festive Mass is celebrated on the feast of the Ascention every year.</p>



<p style="color: black;">&nbsp &nbspAt first the Infant Jesus was placed in the chapel of the Holy Rood. In 1741 because of the great number of pilgrims, it was transfered to the side altar of St. Joachim and St. Anne, opposite the miraculous picture of the Virgin Mary of Mantua. The Altar in its construction shows two lines. The vertical line goes from the Holy Ghoste via God the Father to the Child Jesus. The other - horizontal line passes from the Virgin Mary over the Child Jesus to St. Joseph. These represent the two levels of the human and the divine family. At that time small silver pieces like links were placed around as signs of thanksgiving. This peaceful time did not last long. After the death of the Empress Mary Theresa, her son Joseph II. followed her on the throne in Austria. He abolished the Carmelite monastery on July 3rd 1784. The church became a parish, that was administred by the Maltese Knights.
In the 19th century the fame of the miraculous Infant Jesus spread to the Spanish speaking countries of South America and Italy. And not only there! In some monastries they used a special liturgical prayer to the Infant Jesus of Prague. Many far away Asiatic countries are thanking the Infant Jesus for miracles, like the Philippines, China or Vietnam.
