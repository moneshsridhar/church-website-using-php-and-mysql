<h3 style="color:black;text-align: left; font-weight: bold;">THE 30YEARS WAR - BAROQUE ERA</h3>

<p style="color: black">&nbsp &nbsp &nbspHowever, the devotion to the Divine Infant was short-lived. During the <b>Thirty-years war</b>, the Saxons and the Swedes took turns to invade Prague and pillaged both the church and the monastry; the special devotion held before the image was gradually neglected; the prosperity of the community. declined and need and distress was felt; the Carmelites, the other novices, and the most fervent worshipper of the Infant Jesus of Prague had departed; and the veneration to the Holy Child totally and completely ceased.
With the Carmelite Monastry plundered, the image of the Infant Jesus of Prague was thrown upon a heap of rubbish behind the high altar.<b> Both hands were broken</b> off by the fall, but though made of wax, it was otherwise<b> undamaged</b>. Here the Divine Infant lay, stayed and forgotten by all for several years.
